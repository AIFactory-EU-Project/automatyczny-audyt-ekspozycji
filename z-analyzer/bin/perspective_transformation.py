def transform(pth, ip, category):
    set_name = ip.split(".")[2]
    set_data = quick_meals if category == "quick_snack" else ready_meals
    pts = set_data.get(set_name, None)
    if pts is None:
        raise Exception("The IP address is not valid!")

    all_pts = np.array(pts["original_pts"]).astype(np.float32)
    _, _, orig_w, orig_h = cv2.boundingRect(all_pts.astype(np.int))
    ratio = orig_w / orig_h
    dst_pts = np.array(pts["dst_pts"]).astype(np.float32)
    w, h = dst_pts[2]
    h = int(w / ratio)

    img = cv2.imread(pth)
    if img is None:
        raise Exception("The path to the image is not valid!")

    mtx = cv2.getPerspectiveTransform(all_pts, dst_pts)
    warped = cv2.warpPerspective(img, mtx, (w, h), borderValue=(0, 0, 0))

    cv2.imwrite(pth, warped)


if __name__ == '__main__':
    parser = argparse.ArgumentParser(description="Image's perspective transformation")
    parser.add_argument("path", type=str, help="A path to the image")
    parser.add_argument("ip", type=str, help="An IP address of the camera")
    parser.add_argument("category", type=str, help="A category of segment; possible values: ready, quick")

    args = parser.parse_args()

    transform(args.path, args.ip, args.category)
